sub new
{
  my ($class)=@_;
  
  my $self={};
  bless $self,$class;

  return $self;
}
#---------------------------------------------------------------------
#   my $alignment=$alignmentReader->read($filename); # 3-element array
sub read
  {
    my ($self,$filename)=@_;
    my @array;
    open(IN,$filename) || die "can't open $filename in AlignmentReader\n";
    while(<IN>)
      {
	if(/^Query: (.*)/)
	  {
	    $array[0].=$1;

	    $_=<IN>;
	    $_=~/       (.*)/ || die $_;
	    $array[1].=$1;

	    $_=<IN>;
	    $_=~/Sbjct: (.*)/ || die $_;
	    $array[2].=$1;
	  }
      }
    close(IN);
    return \@array;
  }
