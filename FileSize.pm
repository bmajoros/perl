#---------------------------------------------------------------------
#                           PUBLIC METHODS
#---------------------------------------------------------------------
sub fileSize
  {
    my ($filename)=@_;
    my @stats=stat $filename;
    return $stats[7];
  }
#---------------------------------------------------------------------






#---------------------------------------------------------------------
#                         PRIVATE METHODS
#---------------------------------------------------------------------

1;

